function initMap() {
    const mapParams = {
        center: {lat: 50.006992, lng: 19.963408},
        zoom: 16,
        disableDefaultUI: true,
        styles: style,
    };

    const map = new google.maps.Map(document.querySelector('.contact__mapCont'), mapParams);

    let markerSize = new google.maps.Size(40, 65);
    if(window.innerWidth < 769) {
        markerSize = new google.maps.Size(30, 40)
    }

    const marker = new google.maps.Marker({
        position: new google.maps.LatLng(50.006992, 19.963408),
        map: map,
        animation: google.maps.Animation.DROP,
        icon: {
            url: './img/icons/map_pin.svg',
            scaledSize: markerSize,
        }
    });

    google.maps.event.addListener(
        marker,
        'click',
        ()=>window.open('https://www.google.com/maps/dir/?api=1&destination=QVB&destination_place_id=ChIJB2pZSuRDFkcRaBwtWhepXA8', '_blank')
    );
}
